package com.theuslino.ifstatement.examples;

public class IfStatementRunner {
    public static void main(String[] args) {
        int i = 26;

        if(i == 24){
            System.out.println("i = 24.");
        } else if(i == 25) {
            System.out.println(("i = 25."));
        } else {
            System.out.println("i is not 24 or 25.");
        }
    }
}

